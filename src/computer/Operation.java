package computer;

/**
 *
 * @author jefy
 */
public class Operation {

    public static float Operation(float num1,float num2,String operation) {
        float result = 0;
        switch (operation) {
            case "+":
                result = num1 + num2;
                break;
            case "-":
                result = num1 - num2;
                break;
            case "*":
                result = num1 * num2;
                break;
            case "/":
                result = num1 / num2;
                break;
        }
        return result;
    }
    
}
